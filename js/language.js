const language = {
    ru,
    en,
    uk
}
class ControllLanguage {
    
    constructor () {
        this.language = new ControllCookie().get('language') || 'ru';
    }

    getText({ type, text, lang }){
        let result = '';
        if (type){
            result = text ? language[lang || this.language][type][text] : language[lang || this.language][type];
        }
        else{
            result = 'Not found';
        }
        return result;
    }

}